    //% blockId=sh1107_init
    //% block="initialize OLED"
    //% group="SH1107 OLED"
    export function init() {

        let commands = [
            0xAE,
            0xD5,0x80,
            0xA8,0x5F,
            0xD3,0x00,
            0x40,
            0xAD,0x8B,
            0xA1,
            0xC8,
            0xDA,0x12,
            0x81,0xFF,
            0xD9,0x22,
            0xDB,0x35,
            0xA4,
            0xA6,
            0xAF
        ]

        for (let c of commands) {
            command(c)
        }

        clear()
        show()
    }


    function command(value:number) {

        let data = pins.createBuffer(2)

        data[0] = 0x00
        data[1] = value

        pins.i2cWriteBuffer(address,data)
    }



    //% blockId=sh1107_clear
    //% block="clear OLED"
    //% group="SH1107 OLED"
    export function clear() {

        for(let i = 0; i < 1152; i++) {
            buffer[i] = 0
        }
    }



    //% blockId=sh1107_pixel
    //% block="draw pixel x $x y $y"
    //% group="SH1107 OLED"
    export function pixel(x:number,y:number) {

        if(x < 0 || x >= 96)
            return

        if(y < 0 || y >= 96)
            return


        let page = Math.idiv(y,8)

        let index = page * 96 + x

        buffer[index] |= (1 << (y % 8))
    }



    //% blockId=sh1107_show
    //% block="update OLED"
    //% group="SH1107 OLED"
    export function show() {

        for(let page = 0; page < 12; page++) {

            command(0xB0 + page)

            command(0x02)
            command(0x10)


            let data = pins.createBuffer(97)

            data[0] = 0x40


            for(let x = 0; x < 96; x++) {

                data[x+1] = buffer[(page * 96) + x]

            }


            pins.i2cWriteBuffer(address,data)
        }
    }



    /**
     * Display text
     */
    //% blockId=sh1107_show_text
    //% block="show text $text"
    //% group="SH1107 OLED"
    export function showText(text:string) {

        let x = 0
        let y = 0


        for(let i = 0; i < text.length; i++) {

            let charCode = text.charCodeAt(i)
            let index = -1


            // A-Z
            if(charCode >= 65 && charCode <= 90) {
                index = charCode - 64
            }

            // a-z
            else if(charCode >= 97 && charCode <= 122) {
                index = charCode - 96 + 26
            }

            // Space
            else if(charCode == 32) {
                index = 0
            }


            if(index >= 0) {

                for(let col = 0; col < 5; col++) {

                    let line = font[index][col]


                    for(let row = 0; row < 8; row++) {

                        if((line & (1 << row)) != 0) {

                            pixel(
                                x + col,
                                y + row
                            )

                        }
                    }
                }


                x += 6
            }
        }


        show()
    }

